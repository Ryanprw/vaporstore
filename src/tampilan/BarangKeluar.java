    private void bprosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bprosesActionPerformed
        String sql = "insert into dbkeluar values (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtkdbarang.getText());
            stat.setString(2, txtnamabarang1.getText());
            stat.setString(3, txtktegori.getText());
            stat.setString(4, txtmitra1.getText());
            stat.setString(5, txthargabarang.getText());
            stat.setString(6, txtstockbarang.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            txtkdbarang.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
        }
    }//GEN-LAST:event_bprosesActionPerformed

