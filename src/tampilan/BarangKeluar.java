    protected void datatable(){
        Object[] Baris = {"Kode Barang","Nama Barang", "Kategori", "Mitra", "Harga", "Barang Keluar"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelkeluar.setModel(tabmode);
        String sql = "select * from dbkeluar";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("kode_barang");
                String b = hasil.getString("nama_barang");
                String c = hasil.getString("kategori");
                String d = hasil.getString("mitra");
                String e = hasil.getString("harga");
                String f = hasil.getString("stock_barang");
                
                String[] data={a,b,c,d,e,f};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            
        }
    }

